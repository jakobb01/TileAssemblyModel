package com.jakobbeber.tam;

public class Duo {

    private int first;
    private int second;

    public Duo(int first, int second) {
        this.first = first;
        this.second = second;
    }

    // Getters and setters for the Triple class

    public int getFirst() {
        return first;
    }

    public void setFirst(int first) {
        this.first = first;
    }

    public int getSecond() {
        return second;
    }

    public void setSecond(int second) {
        this.second = second;
    }

}
